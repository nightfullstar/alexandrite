{#if badges.length}
	<span class="sx-font-size-2 f-row">
		{#each badges as b}
			<Tooltip>
				<span slot="tooltip">{b.text}</span>
				<span class="sx-badge-{b.color} ws-nowrap">
					{#if b.icon}
						<Icon icon={b.icon} />
					{/if}
					<span class:sr-only={!!b.icon}>{b.text}</span>
				</span>
			</Tooltip>
		{/each}
	</span>
{/if}

<script lang="ts">
	import { Icon, Tooltip } from 'sheodox-ui';
	import type { Post } from 'lemmy-js-client';

	export let post: Post;

	$: badges = getBadges(post);

	function getBadges(post: Post) {
		const badges = [];

		if (post.nsfw) {
			badges.push({ color: 'red', text: 'NSFW' });
		}
		if (post.locked) {
			badges.push({ color: 'orange', text: 'Kilitlendi', icon: 'lock' });
		}
		if (post.deleted) {
			badges.push({ color: 'red', text: 'Silindi', icon: 'trash' });
		}
		if (post.featured_local) {
			badges.push({ color: 'orange', text: 'Lokalde Öne Çıkan', icon: 'thumbtack' });
		}
		if (post.featured_community) {
			badges.push({ color: 'green', text: 'Toplulukta Öne Çıkan', icon: 'thumbtack' });
		}
		return badges;
	}
</script>
